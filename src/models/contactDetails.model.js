import mongoose, { Schema } from "mongoose";

const contactDetailSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

export const ContactDetail = mongoose.model(
  "ContactDetail",
  contactDetailSchema
);
